class Person {
  constructor(id, name, lastname, birthdate, dni, address, phone, email) {
    this.id = id;
    this.name = name;
    this.lastname = lastname;
    this.birthdate = birthdate;
    this.dni = dni;
    this.address = address;
    this.phone = phone;
    this.email = email;
  }
}

export default Person;
