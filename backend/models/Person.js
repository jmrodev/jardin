class Person {
  constructor(id, personType, name, lastname, dni, address, phone, email, birthdate) {
    this.id = id;
    this.personType = personType;
    this.name = name;
    this.lastname = lastname;
    this.dni = dni;
    this.address = address;
    this.phone = phone;
    this.email = email;
    this.birthdate = birthdate;
  }
}

export default Person;
